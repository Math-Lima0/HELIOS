# ─────────────────────────────────────────────────────────────────────────────
#  CONSTRUÇÃO DOS INTERPOLADORES (executado uma vez na importação)
# ─────────────────────────────────────────────────────────────────────────────

# Grade uniforme em log(x) e log(Rs/nRL)
_LOG_X  = np.linspace(math.log10(1), math.log10(1000), 300)
_CURVES = sorted(_RAW_FIG3.keys())
_LOG_RS = np.array([math.log10(c) for c in _CURVES])


def _build_grid(raw_data, log_y: bool) -> np.ndarray:
    """
    Para cada curva de Rs/nRL, ajusta um CubicSpline no espaço log(x)
    e avalia na grade _LOG_X. Se log_y=True trabalha em log(y).
    Retorna matriz (n_curvas × n_x).
    """
    grid = np.zeros((len(_CURVES), len(_LOG_X)))
    for i, rs in enumerate(_CURVES):
        pts = raw_data[rs]
        lx = np.array([math.log10(p[0]) for p in pts])
        y  = np.array([p[1] for p in pts])
        if log_y:
            y = np.log10(y)
        cs = CubicSpline(lx, y, extrapolate=True)
        grid[i] = cs(_LOG_X)
    return grid


_GRID_F3 = _build_grid(_RAW_FIG3, log_y=False)   # Y linear
_GRID_F4 = _build_grid(_RAW_FIG4, log_y=True)    # Y log

# RegularGridInterpolator opera em (log_rs, log_x) → y (no espaço transformado)
_INTERP_F3 = RegularGridInterpolator(
    (_LOG_RS, _LOG_X), _GRID_F3,
    method="linear", bounds_error=False, fill_value=None
)
_INTERP_F4 = RegularGridInterpolator(
    (_LOG_RS, _LOG_X), _GRID_F4,
    method="linear", bounds_error=False, fill_value=None
)


# ─────────────────────────────────────────────────────────────────────────────
#  API PÚBLICA
# ─────────────────────────────────────────────────────────────────────────────

def fig3(x: float, rs_nrl_pct: float) -> float:
    """
    Retorna IFrms/Ifav interpolado da Figura 3.

    Parâmetros
    ----------
    x           : n · ωs · RL · C
    rs_nrl_pct  : Rs / (n · RL)  em %

    Retorno
    -------
    Razão IFrms/Ifav  (adimensional)
    """
    rs_nrl_pct = max(_CURVES[0],  min(_CURVES[-1],  rs_nrl_pct))
    x          = max(1.0,         min(1000.0,        x))
    lrs = math.log10(rs_nrl_pct)
    lx  = math.log10(x)
    return float(_INTERP_F3([[lrs, lx]])[0])


def fig4(x: float, rs_nrl_pct: float) -> float:
    """
    Retorna IFrm/Ifav interpolado da Figura 4.

    Parâmetros
    ----------
    x           : n · ωs · RL · C
    rs_nrl_pct  : Rs / (n · RL)  em %

    Retorno
    -------
    Razão IFrm/Ifav  (adimensional)
    """
    rs_nrl_pct = max(_CURVES[0],  min(_CURVES[-1],  rs_nrl_pct))
    x          = max(1.0,         min(1000.0,        x))
    lrs = math.log10(rs_nrl_pct)
    lx  = math.log10(x)
    log_y = float(_INTERP_F4([[lrs, lx]])[0])
    return 10 ** log_y


